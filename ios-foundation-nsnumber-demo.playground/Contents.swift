import UIKit

let number: NSNumber = 3.14

/*
 NSNumberはInt, Float, Double, Bool型へ変換可能
 */

// Int
let int = number as Int
type(of: int)

// Float
let float = number as Float
type(of: float)

// Double
let double = number as Double
type(of: double)

// Bool
let bool = number as Bool
type(of: bool)
